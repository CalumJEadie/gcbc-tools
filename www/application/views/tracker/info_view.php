<h3>2012 Erg</h3>

<p>This app is the primary way we'll be recording our progress for the 2012 erg. It links to the main site which will show our up to date fundraising and distance progress. Check out the running total at <a href="http://www.girtoncollegeboatclub.com">http://www.girtoncollegeboatclub.com</a>.</p>

<h4>Accidental Deletion</h4>

<p>Any team, participant or leg that has been accidentally deleted can be restored however if we're careful that shouldn't be a problem! </p>

<h4>Paper Backup</h4>

<p>The information entered into this app is backed up every 5 mins so we won't need to run a paper backup throughout however if the app is not usable for some reason we'll revert to recording times on paper.</p>

